
/* This is a component class. This class is implemented based on the students and their preferences 
 * of branch for counseling process.
 * @author Bhupendra
 */
public class StudentPreferences {
	// Instance variables.
	String name;
	String first;
	String second;
	String third;
	String fourth;
	String fifth;
	
	// Constructor.
	public StudentPreferences(String name, String first, 
			String second, String third, 
			String fourth, String fifth) {
		this.name = name;
		this.first = first;
		this.second = second;
		this.third = third;
		this.fourth = fourth;
		this.fifth = fifth;
	}
}
