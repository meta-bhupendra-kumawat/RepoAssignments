
public interface Calc {
	
//	Abstract method to Add two given numbers as String.
	/**
	 * @param n1
	 * @param n2
	 * @return
	 */
	String add(String n1, String n2);
	
//	Abstract method to Subtract two given numbers as String.
	/**
	 * @param n1
	 * @param n2
	 * @return
	 */
	String subtract(String n1, String n2);
	
//	Abstract method to Multiply two given numbers as String.
	/**
	 * @param n1
	 * @param n2
	 * @return
	 */
	String multiply(String n1, String n2);
	
//	Abstract method to Divide two given numbers as String.
	/**
	 * @param n1
	 * @param n2
	 * @return
	 */
	String divide(String n1, String n2);
}
